    public void showAll_CTHD(){
          DefaultTableModel n = new DefaultTableModel();
        CTHoaDonBUS bus = new CTHoaDonBUS();
        bus.docDSCTHD();
         Vector header = new Vector();
            header.add("STT");
            header.add("Mã HD");
            header.add("Tên SP");
            header.add("Đơn giá");
            header.add("Số lượng");
            header.add("Tổng tiền");
            header.add("Ngày lập");
            header.add("Tên thành viên");
            header.add("Tên nhân viên");
            header.add("Ghi chú");
           
            n = new DefaultTableModel(header,0);
            int k = 0;
             for (CTHoaDon_DTO nv : CTHoaDonBUS.ds_cthd) {
     
            Vector row = new Vector();
            row.add(k);
            row.add(nv.id_hoadon);
            row.add(nv.tensp);          
            row.add(nv.dongia);
            row.add(nv.soluong);
            row.add(nv.tongtien);
            row.add(nv.ngaylap);
            row.add(nv.tentv);
            row.add(nv.tennv);
            row.add(nv.ghichu);
            n.addRow(row);
            k++;
        }
        tbl_thongke.setModel(n);
    }
